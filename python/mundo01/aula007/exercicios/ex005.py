# Operadores aritméticos
# Faça um programa que leia o número inteiro e mostre na tela o seu sucessor e seu antecessor.
n = int(input('Digite um número inteiro: '))
a = n - 1
s = n + 1
print(f'Analisando o número \033[4:34m{n}\033[m:'
      f'\nSeu antecessor é \033[4:31m{a}\033[m.'
      f'\nE seu sucessor é \033[4:32m{s}\033[m.')
